def sumar(num1: int, num2: int) -> int:
    """Returns addition of two numbers

    :param num1: First number
    :type num1: int
    :param num2: Second number
    :type num2: int
    :return: Addition of num1 and num2
    :rtype: int
    """
    result = num1 + num2
    return result


def restar(num1: int, num2: int) -> int:
    """Returns subtraction of two numbers

    :param num1: First number
    :type num1: int
    :param num2: Second number
    :type num2: int
    :return: Subtraction of num1 and num2
    :rtype: int
    """
    result = num1 - num2
    return result


def multiplicar(num1: int, num2: int) -> int:
    """Returns multiplication of two numbers

    :param num1: First number
    :type num1: int
    :param num2: Second number
    :type num2: int
    :return: Multiplication of num1 and num2
    :rtype: int
    """
    result = num1 * num2
    return result


def dividir(num1: int, num2: int) -> int:
    """Returns division of two numbers

    :param num1: First number
    :type num1: int
    :param num2: Second number
    :type num2: int
    :return: Division of num1 and num2
    :rtype: int
    """
    result = num1 / num2
    return result
